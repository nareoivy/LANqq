package com.chat.bean;

public class Friend {
	
	private String userNo;
	private String friendNos;
	public String getUserNo() {
		return userNo;
	}
	public void setUserNo(String userNo) {
		this.userNo = userNo;
	}
	public String getFriendNos() {
		return friendNos;
	}
	public void setFriendNos(String friendNos) {
		this.friendNos = friendNos;
	}
	

}
